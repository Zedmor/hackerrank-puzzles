def search(x, y, recur_depth=0):
    print('visiting %d,%d' % (x, y))

    if grid[y][x] == 1:
        print('wall at %d,%d' % (x, y))
        return False
    elif grid[y][x] == 3:
        print('visited at %d,%d' % (x, y))
        return False

    # mark as visited
    grid[y][x] = 3
    depths.add(recur_depth)

    # explore neighbors clockwise starting by the one on the right
    if ((x < len(grid) - 1 and search(x + 1, y, recur_depth=recur_depth + 1))
            or (y > 0 and search(x, y - 1, recur_depth=recur_depth + 1))
            or (x > 0 and search(x - 1, y, recur_depth=recur_depth + 1))
            or (y < len(grid) - 1 and search(x, y + 1, recur_depth=recur_depth + 1))):
        return True

    return False

